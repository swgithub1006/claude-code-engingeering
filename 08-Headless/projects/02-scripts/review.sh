#!/bin/bash
# review.sh - 代码审查脚本
#
# 使用 Claude Code 对指定文件或目录进行代码审查
#
# 用法:
#   ./review.sh <file_or_directory>
#   ./review.sh src/
#   ./review.sh src/app.ts
#
# 环境变量:
#   ANTHROPIC_API_KEY - Anthropic API Key (必需)
#
# 输出:
#   审查结果输出到 stdout 和 review-report.md

set -e

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# 检查参数
TARGET=${1:-.}

if [ ! -e "$TARGET" ]; then
    echo -e "${RED}Error: $TARGET does not exist${NC}"
    exit 1
fi

# 检查 API Key
if [ -z "$ANTHROPIC_API_KEY" ]; then
    echo -e "${RED}Error: ANTHROPIC_API_KEY not set${NC}"
    echo "Please set your API key:"
    echo "  export ANTHROPIC_API_KEY='your-key'"
    exit 1
fi

# 检查 Claude Code 是否安装
if ! command -v claude &> /dev/null; then
    echo -e "${RED}Error: Claude Code not installed${NC}"
    echo "Install with: npm install -g @anthropic-ai/claude-code"
    exit 1
fi

echo -e "${GREEN}Starting code review for: $TARGET${NC}"
echo ""

# 构建文件列表
if [ -d "$TARGET" ]; then
    FILES=$(find "$TARGET" -type f \( -name "*.ts" -o -name "*.js" -o -name "*.py" -o -name "*.go" -o -name "*.rs" \) | head -20)
    FILE_COUNT=$(echo "$FILES" | wc -l)
    echo -e "${YELLOW}Found $FILE_COUNT files to review${NC}"
else
    FILES="$TARGET"
    FILE_COUNT=1
fi

echo "$FILES"
echo ""

# 运行审查
echo -e "${GREEN}Running Claude Code review...${NC}"
echo ""

PROMPT="Review the following code files for quality and issues:

Files to review:
$FILES

Please analyze for:
1. Code quality and readability
2. Potential bugs and edge cases
3. Security vulnerabilities
4. Performance issues
5. Best practices violations

Provide a detailed report with:
- Summary of findings
- Specific issues with file:line references
- Recommendations for improvement
- Overall code quality score (1-10)"

# 执行审查并保存结果
RESULT=$(claude -p "$PROMPT" \
    --output-format text \
    --max-turns 15 \
    --allowedTools Read,Grep,Glob)

# 输出结果
echo ""
echo "============================================"
echo "               REVIEW REPORT                "
echo "============================================"
echo ""
echo "$RESULT"

# 保存到文件
REPORT_FILE="review-report-$(date +%Y%m%d-%H%M%S).md"
cat > "$REPORT_FILE" << EOF
# Code Review Report

**Date**: $(date)
**Target**: $TARGET
**Files Reviewed**: $FILE_COUNT

---

$RESULT

---
*Generated by Claude Code*
EOF

echo ""
echo -e "${GREEN}Report saved to: $REPORT_FILE${NC}"
